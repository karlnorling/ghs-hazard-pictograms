/** Returns the contents of the auto-generated pictogram registry file. */
const generatePictogramsFile = (pictograms: Pictogram[]): string => {
  const lines: string[] = [
    `// THIS FILE IS AUTO-GENERATED. DO NOT EDIT MANUALLY.`,
    `// Run 'yarn generate' to regenerate.`,
    ``,
    `import type { Pictogram } from './types';`,
    ``,
    `export const pictograms: Pictogram[] = [`,
  ];

  for (const p of pictograms) {
    lines.push(
      `  {`,
      `    assets: {`,
      `      jpg: ${JSON.stringify(p.assets.jpg)},`,
      `      png: ${JSON.stringify(p.assets.png)},`,
      `      svg: ${JSON.stringify(p.assets.svg)},`,
      `      webp: ${JSON.stringify(p.assets.webp)},`,
      `    },`,
      `    category: ${JSON.stringify(p.category)},`,
      `    code: ${JSON.stringify(p.code)},`,
      `    description: ${JSON.stringify(p.description)},`,
      `    id: ${JSON.stringify(p.id)},`,
      `    name: ${JSON.stringify(p.name)},`,
      `    svg: ${JSON.stringify(p.svg)},`,
      `  },`,
    );
  }

  lines.push(`];`);
  return lines.join('\n') + '\n';
};

