/**
 * generate-source.ts
 *
 * Reads data/scraped.json + assets/svg-map.json and generates:
 *   - src/pictograms.generated.ts        (typed Pictogram registry)
 *   - src/react/PictogramProps.ts        (shared prop interface)
 *   - src/react/{ComponentName}.tsx      (one React FC per pictogram)
 *   - src/react/index.ts                 (barrel re-export)
 *
 * Run via: yarn generate
 */

import fs from 'fs';
import path from 'path';
import { optimize } from 'svgo';
import type { ScrapedData, ScrapedPictogram } from './scrape';
import type {
  Pictogram,
  PictogramCategory,
} from '../packages/@ghs-hazard-pictograms/core/src/types';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/**
 * Converts a string to a URL-safe kebab-case slug.
 * @example slugify('GHS01: Explosive') // 'ghs01-explosive'
 */
const slugify = (str: string): string =>
  str
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');

/**
 * Parses a pictogram title like "GHS01: Explosive" into its code and name parts.
 * Returns an empty code string for transport pictograms that have no GHS code.
 */
const parseTitle = (title: string): { code: string; name: string } => {
  const match = title.match(/^(GHS\d+):\s*(.+)$/i);
  if (match) return { code: match[1].toUpperCase(), name: match[2].trim() };
  return { code: '', name: title.trim() };
};

/** Pixel sizes at which raster variants are generated by create-assets.ts */
const IMAGE_SIZES = [240, 512, 768, 1024, 2048] as const;

/**
 * Builds the {@link Pictogram.assets} record for a pictogram given the relative
 * path to its original SVG file inside assets/.
 */
const buildAssets = (svgRelPath: string): Pictogram['assets'] => {
  const dir = path.dirname(svgRelPath);
  const base = path.basename(svgRelPath, path.extname(svgRelPath));

  const makeRecord = (ext: string): Record<number, string> =>
    Object.fromEntries(IMAGE_SIZES.map((s) => [s, `${dir}/${base}_${s}x${s}.${ext}`])) as Record<
      number,
      string
    >;

  return {
    jpg: makeRecord('jpg'),
    png: makeRecord('png'),
    svg: svgRelPath,
    webp: makeRecord('webp'),
  };
};

/**
 * Strips the XML declaration and DOCTYPE preamble from an SVG string so it
 * can be embedded inline in HTML without triggering parse errors.
 */
const cleanSvg = (svg: string): string =>
  svg
    .replace(/<\?xml[^>]*\?>/g, '')
    .replace(/<!DOCTYPE[^>]*>/g, '')
    .trim();

/**
 * Converts a kebab-case slug to PascalCase for use as a React component name.
 * @example toComponentName('ghs01-explosive') // 'Ghs01Explosive'
 */
const toComponentName = (id: string): string =>
  id
    .split('-')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join('');

/**
 * Runs SVGO optimisation on the given SVG string and returns the optimised result.
 * Uses the default preset with multipass enabled.
 */
const optimizeSvg = (svg: string): string => {
  const result = optimize(svg, { multipass: true, plugins: ['preset-default'] });
  return result.data;
};

// ---------------------------------------------------------------------------
// Pictogram collection
// ---------------------------------------------------------------------------

interface PictogramEntry {
  /** The scraped pictogram data from Wikipedia. */
  category: PictogramCategory;
  /** The resolved SVG map key, or undefined when no matching file was found. */
  pictogram: ScrapedPictogram;
  /** Pictogram category used to populate {@link Pictogram.category}. */
  svgKey: string | undefined;
}

/**
 * Walks the scraped data and pairs each pictogram with its matching SVG key
 * from the SVG map (keyed by relative file path inside assets/).
 */
const collectEntries = (scraped: ScrapedData, svgMap: Record<string, string>): PictogramEntry[] => {
  const entries: PictogramEntry[] = [];
  const svgKeys = Object.keys(svgMap);

  /**
   * Finds the best matching SVG key for a pictogram by comparing base filenames.
   * Returns undefined when no match is found.
   */
  const findSvgKey = (imageUrls: string[]): string | undefined => {
    for (const url of imageUrls) {
      const filename = decodeURIComponent(path.basename(url)).replace(/^File:/, '');
      const filenameBase = path.basename(filename, path.extname(filename)).toLowerCase();
      const match = svgKeys.find(
        (k) => path.basename(k, path.extname(k)).toLowerCase() === filenameBase,
      );
      if (match) return match;
    }

    return undefined;
  };

  /** Processes a flat section of scraped pictogram data into {@link PictogramEntry} objects. */
  const processSection = (data: Record<string, ScrapedPictogram>, category: PictogramCategory) => {
    for (const [, pictogram] of Object.entries(data)) {
      if (!pictogram.images || pictogram.images.length === 0) continue;
      const svgKey = findSvgKey(pictogram.images);
      entries.push({ category, pictogram, svgKey });
    }
  };

  const topLevel: Array<[string, PictogramCategory]> = [
    ['Physical_hazards_pictograms', 'physical_hazards'],
    ['Health_hazards_pictograms', 'health_hazards'],
    ['Physical_and_health_hazard_pictograms', 'physical_and_health_hazards'],
    ['Environmental_hazards_pictograms', 'environmental_hazards'],
  ];

  for (const [key, cat] of topLevel) {
    const section = (scraped as Record<string, unknown>)[key] as {
      data: Record<string, ScrapedPictogram>;
    };
    if (section?.data) processSection(section.data, cat);
  }

  const transport = scraped.Transport_pictograms;
  for (const subKey of Object.keys(transport)) {
    const sub = (transport as Record<string, unknown>)[subKey] as {
      data: Record<string, ScrapedPictogram>;
    };
    if (sub?.data) processSection(sub.data, 'transport');
  }

  return entries;
};

